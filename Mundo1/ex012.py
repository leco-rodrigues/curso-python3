# Faça um algoritmo que leia o preço de um produto e mostre seu novo preço, com 5% de desconto.

# Passo 1: Receber o valor de "preço"
preço = float(input('Quanto custa esse produto? '))

# Preço 2: Calcular o novo valor de "preço", com 5% de desconto (preço * 0.95)
print(f'Com 5% de desconto o preço fica R${preço * 0.95:.2f}!')
