# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

# Passo 1: Receber um valor para "número"
n = float(input('Digite um número: ')) # número

# Passo 2: Calcular a multiplicação e a raiz quadrada
print(f'O dobro de {n} é igual a {n * 2}.\n' # dobro = n * 2
      f'O triplo de {n} é igual a {n * 3}.\n' # triplo = n * 3
      f'A raiz quadrada de {n} é igual a {n ** (1/2)}.') # raiz quadrada = n ** (1/2)
