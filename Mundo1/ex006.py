# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

# Passo 1: Receber um valor de "número"
num = float(input('Digite um número: '))

# Passo 2: Multiplicar esse valor por 2 (num * 2)
print(f'O dobro de {num} é igual a {num * 2}.')

# Passo 3: Multiplicar esse valor por 3 (num * 3)
print(f'O triplo de {num} é igual a {num * 3}.')

# Passo 4: Calcular a raiz quadrada desse valor (num ** (1/2))
print(f'A raiz quadrada de {num} é igual a {num ** (1/2)}.')
