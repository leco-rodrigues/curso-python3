# Escreva um programa que converta uma temperatura digitada em °C e converta para °F.

# Passo 1: Receber um valor para "Celcius"
c = float(input('Digite a temperatura em °C: ')) # Celsius

# Passo 2: Converter para "Fahrenheit"
print(f'A temperatura de {c:.1f}°C, convertida em Fahrenheit, corresponde a {(c * 1.8) + 32:.1f}°F.') # Fahrenheit = Celsius * 1.8 + 32
