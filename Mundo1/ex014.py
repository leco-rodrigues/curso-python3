# Escreva um programa que converta uma temperatura digitada em °C e converta para °F.

# Passo 1: Receber um valor para "Celcius"
celsius = float(input('Digite a temperatura em °C: '))

# Passo 2: Converter esse valor para "Fahrenheit" ((c * 1.8) + 32)
print(f'A temperatura de {celsius:.1f}°C, convertida em Fahrenheit, corresponde a {(celsius * 1.8) + 32:.1f}°F.')
