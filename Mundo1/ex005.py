# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.

# Passo 1: Receber um valor para "número"
n = float(input('Digite um número: ')) # número

# Passo 2: Calcular o antecessor e o sucessor
print(f'O antecessor de {n} é {n - 1}. ' # antecessor = n - 1
      f'E seu sucessor é {n + 1}.') # sucessor = n + 1
