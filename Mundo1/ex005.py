# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.

# Passo 1: Receber um valor de "número"
num = float(input('Digite um número: '))

# Passo 2: Mostrar o antecessor desse valor (n - 1)
print(f'O antecessor de {num} é {num - 1}.')

# Pass 3: Mostrar o sucessor desse valor (n + 1)
print(f'O sucessor de {num} é igual a {num + 1}.')
