# Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros.

# Passo 1: Receber um valor de "metros"
m = float(input('Digite uma distância em metros: '))

# Passo 2: Converter esse valor em "centímetros" (m * 100)
print(f'{m} metros equivalem a {m * 100} centímetros.')

# Passo 3: Converter esse valor em "milímetros" (m * 1000)
print(f'{m} metros equivalem a {m * 1000} milímetros.')

    # Extra: Converter esse valor em quilômetros (km), hectômetros (hm), decâmetros (dam) e decímetros (dm).

# Passo 4: Converter esse valor em "quilômetros" (m / 1000)
print(f'{m} metros equivalem a {m / 1000} quilômetros.')

# Passo 5: Converter esse valor em "hectômetros" (m / 100)
print(f'{m} metros equivalem a {m / 100} hectômetros.')

# Passo 6: Converter esse valor em "decâmetros" (m / 10)
print(f'{m} metros equivalem a {m / 10} decâmetros.')

# Passo 7: Converter esse valor em "decímetros" (m * 10)
print(f'{m} metros equivalem a {m * 10} decímetros.')
