# Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros.

# Passo 1: Receba um valor de "metros"
m = float(input('Digite uma quantidade de metros: '))

# Passo 2: Converter esse valor em "centímetros" (m * 100)
print(f'{m} metros equivalem a {m * 100} centímetros.')

# Passo 3: Converter esse valor em "milímetros" (m * 1000)
print(f'{m} metros equivalem a {m * 1000} milímetros.')
