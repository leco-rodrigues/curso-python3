# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário, com 15% de aumento.

# Passo 1: Receber um valor de "salário"
salário = float(input('Qual é o seu salário? R$'))

# Passo 2: Calcular o novo salário com 15% de aumento (salário * 1.15)
print(f'R${salário}? O seu novo salário é de R${salário * 1.15:.2f}! Um aumento de 15%!')
