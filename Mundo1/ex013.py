# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário, com 15% de aumento.

# Passo 1: Receber um valor para "salário"
salario = float(input('\033[7;40mQual é o seu salário atual?\033[m R$'))

# Passo 2: Exibir novo salário com aumento
print(f'\033[1;34mR${salario:.2f}? O seu novo salário será de R${salario * 1.15:.2f}! Um aumento de 15%!\033[m')
