# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário, com 15% de aumento.

# Passo 1: Receber um valor para "salário"
s = float(input('Qual é o seu salário atual? R$')) # salário

# Passo 2: Calcular o novo salário
print(f'R${s}? O seu novo salário é de R${s * 1.15:.2f}! Um aumento de 15%!') # novo salário = salário * 1.15
