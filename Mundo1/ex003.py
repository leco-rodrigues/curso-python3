# Crie um programa que leia dois números e mostre a soma entre eles.

# Passo 1: Receber valores para dois "números"
n1 = float(input('\033[7;40mDigite o valor do primeiro número:\033[m '))
n2 = float(input('\033[7;40mDigite o valor do segundo número:\033[m '))

# Passo 2: Exibir a soma
print(f'\033[1;34m{n1} + {n2} = {n1 + n2}.\033[m')
