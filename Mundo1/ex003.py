# Crie um programa que leia dois números e mostre a soma entre eles.

# Passo 1: Receber os valores para "dois números"
n1 = float(input('Digite o valor do primeiro número: ')) # primeiro número
n2 = float(input('Digite o valor do segundo número: ')) # segundo número

# Passo 2: Calcular a soma
print(f'A soma entre {n1} e {n2} é igual a {n1 + n2}.') # soma = n1 + n2
